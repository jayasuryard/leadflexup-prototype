export const t = (key, lang = 'en') => {
  return translations[lang]?.[key] || translations.en[key] || key;
};

export const getLocalizedText = (obj, lang = 'en') => {
  if (!obj) return '';
  if (typeof obj === 'string') return obj;
  return obj[lang] || obj.en || '';
};
